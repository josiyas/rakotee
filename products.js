function setSelectOptions(selectEl, allLabel, values) {
  if (!selectEl) return;
  const previous = selectEl.value;
  const sorted = [...values].sort((a, b) => a.localeCompare(b));

  selectEl.innerHTML = '';
  const allOption = document.createElement('option');
  allOption.value = '';
  allOption.textContent = allLabel;
  selectEl.appendChild(allOption);

  sorted.forEach((value) => {
    const option = document.createElement('option');
    option.value = value;
    option.textContent = value;
    selectEl.appendChild(option);
  });

  if (sorted.includes(previous)) {
    selectEl.value = previous;
  }
}

function setSelectOptionsWithLabels(selectEl, allLabel, options) {
  if (!selectEl) return;
  const previous = selectEl.value;

  selectEl.innerHTML = '';
  const allOption = document.createElement('option');
  allOption.value = '';
  allOption.textContent = allLabel;
  selectEl.appendChild(allOption);

  options.forEach(({ value, label }) => {
    const option = document.createElement('option');
    option.value = value;
    option.textContent = label;
    selectEl.appendChild(option);
  });

  if (options.some((o) => o.value === previous)) {
    selectEl.value = previous;
  }
}

function buildFilterValues(sourceProducts) {
  const categories = new Set();
  const colors = new Set();
  const sizes = new Set();

  sourceProducts.forEach((p) => {
    if (p.category) categories.add((p.category || '').toString().trim());
    (p.colors || []).forEach((c) => colors.add((c || '').toString().trim()));
    (p.sizes || []).forEach((s) => sizes.add((s || '').toString().trim()));
  });

  return { categories, colors, sizes };
}

function getCategoryOptionsWithProducts() {
  return initialCategoryOptions
    .filter((option) => option.value)
    .filter((option) => {
      const byCategoryField = products.some(
        (p) => normalizeFilterValue(p.category) === normalizeFilterValue(option.value)
      );
      const byFallbackMapping = getCategoryProducts(option.value).length > 0;
      return byCategoryField || byFallbackMapping;
    });
}

function refreshFilterOptions() {
  const selectedCategory = normalizeFilterValue(categoryFilter ? categoryFilter.value : '');
  const sourceForColorAndSize = selectedCategory
    ? products.filter((p) => normalizeFilterValue(p.category) === selectedCategory)
    : products;

  const scopedValues = buildFilterValues(sourceForColorAndSize);

  setSelectOptionsWithLabels(categoryFilter, 'All Categories', getCategoryOptionsWithProducts());
  setSelectOptions(colorFilter, 'All Colors', scopedValues.colors);
  setSelectOptions(sizeFilterEl, 'All Sizes', scopedValues.sizes);
}

function filterProducts() {
  const term = (searchInput.value || '').toLowerCase();
  const color = normalizeFilterValue(colorFilter.value);
  const size = normalizeFilterValue(sizeFilter.value);
  const category = normalizeFilterValue(categoryFilter ? categoryFilter.value : '');
  let filtered = products.filter(p => {
    const matchesTerm = p.name.toLowerCase().includes(term);
    const productColors = (p.colors || []).map(normalizeFilterValue);
    const productSizes = (p.sizes || []).map(normalizeFilterValue);
    const matchesColor = (color === '' || productColors.includes(color));
    const matchesSize = (size === '' || productSizes.includes(size));
    let matchesCategory = true;
    if (category && category !== '') {
      if (p.category) {
        matchesCategory = normalizeFilterValue(p.category) === category;
      } else {
        // fallback to name-based mapping
        matchesCategory = getCategoryProducts(category).some(x => x.id === p.id);
      }
    }
    return matchesTerm && matchesColor && matchesSize && matchesCategory;
  });
  displayProducts(filtered);
}

